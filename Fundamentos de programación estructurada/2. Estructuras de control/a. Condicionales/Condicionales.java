// Usando solo ifs
if (temperatura > 25) {
    System.out.println("A la playa!!!");
}

if (temperatura <= 25) {
    System.out.println("Esperando al buen tiempo...");
}

// Usando if con else
// Si la temperatura es mayor que 25 ... y si no ...
if (temperatura > 25) {
    System.out.println("A la playa!!!");
} else {
    System.out.println("Esperando al buen tiempo...");
}